package com.assignment.sgotur.employee.entity;

public class Employee {

	private int empID;
	private String name;

	public int get_empID() {
		return empID;
	}

	public void set_empID(int empID) {
		this.empID = empID;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}